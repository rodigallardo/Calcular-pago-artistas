    private void llenarComboBox() {
        cmb_genero.setModel(new DefaultComboBoxModel<>(Genero.values()));
    }

    ArrayList<Artista> lista_artistas = new ArrayList<>();

    public void cargarLista() {
        DefaultListModel<Artista> model = new DefaultListModel<>(); // Modelo para el JList
        // Iterar sobre la lista
        for (Artista item : lista_artistas) { //Por cada elemento item representa un elemento de lista_personas
            model.addElement(item); // Agregar cada persona al modelo
        }
        list_calculos.setModel(model);
    }
