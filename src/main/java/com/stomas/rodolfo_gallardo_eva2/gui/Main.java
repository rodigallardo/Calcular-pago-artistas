    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Artista a = new Artista();
            a.setNombre(txt_nombre.getText());
            a.setCant_canciones_subidas(Integer.parseInt(txt_cant_canc.getText()));
            a.setCant_reproducciones(Integer.parseInt(txt_cant_repro.getText()));
            
            // Obtener el género seleccionado del combo box
            Genero generoSeleccionado = (Genero) cmb_genero.getSelectedItem();
            // Si el género es válido, asignarlo al artista
            a.setEnum_genero(generoSeleccionado);
            
            lb_resultado.setText(a.calcularPago() + "");
            
            lista_artistas.add(a);
            cargarLista();
        } catch (ArtistaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

